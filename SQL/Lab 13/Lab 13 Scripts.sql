--Demonstration of a Join

SELECT PACKAGE_ID, TRANSPORTATION.TRANSPORT_ID, TRANS_TYPE
FROM PACKAGE JOIN TRANSPORTATION 
	ON PACKAGE.TRANSPORT_ID=TRANSPORTATION.TRANSPORT_ID;

--Two Level Select Statement

SELECT BOOKING_ID, HOTEL_NAME, COST_PER_ROOM
FROM HOTEL, BOOKING
WHERE HOTEL.HOTEL_ID=BOOKING.HOTEL_ID AND
COST_PER_ROOM<=(SELECT AVG(COST_PER_ROOM)
			FROM BOOKING
			WHERE HOTEL.HOTEL_ID=BOOKING.HOTEL_ID);


