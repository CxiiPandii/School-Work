DROP TABLE FLIGHTS;
CREATE TABLE "FLIGHTS"
   ("FLIGHT_NUM" VARCHAR2(7),
    "PLANE_TYPE" VARCHAR2(20),
    "AIRLINE" VARCHAR2(35),
    "FLIGHT_START" TIMESTAMP (0) ,
    "FLIGHT_END" TIMESTAMP (0),
    "F_ORIGIN" VARCHAR2(5),
    "F_DESINATION" VARCHAR2(5),
     CONSTRAINT "F_NUM_PK" PRIMARY KEY ("FLIGHT_NUM") 
   );

DROP TABLE TRANSPORTATION;
CREATE TABLE TRANSPORTATION
(TRANSPORT_ID NUMBER(5,0),
 TRANS_TYPE VARCHAR(15),
 TRANS_START TIMESTAMP(0),
 TRANS_END TIMESTAMP(0),
 CONSTRAINT "TR_ID_PK" PRIMARY KEY ("TRANSPORT_ID") 
);

DROP TABLE HOTEL;
CREATE TABLE HOTEL
(HOTEL_ID NUMBER (6,0),
 HOTEL_NAME VARCHAR (40),
 HOTEL_TYPE VARCHAR (30),
 HOTEL_STREET VARCHAR (30),
 HOTEL_STATE_PROV VARCHAR (40),
 HOTEL_COUNTRY VARCHAR (30),
 CONSTRAINT "HOT_ID_PK" PRIMARY KEY ("HOTEL_ID") 
);


DROP TABLE RESTAURANT;
CREATE TABLE RESTAURANT
(REST_ID NUMBER (5,0),
 REST_NAME VARCHAR (30),
 REST_STREET VARCHAR (45),
 REST_STATE_PROV VARCHAR (20),
 REST_COUNTRY VARCHAR (30),
 CONSTRAINT "REST_ID_PK" PRIMARY KEY ("REST_ID") 
);




DROP TABLE ACTIVITY;
CREATE TABLE ACTIVITY
(ACT_ID NUMBER(4,0),
 ACT_NAME VARCHAR(100),
 ACT_TYPE VARCHAR(20),
 ACT_START TIMESTAMP(0),
 ACT_END TIMESTAMP (0),
 ACT_COST_PER_PERSON NUMBER (7,2),
 CONSTRAINT "ACT_ID_PK" PRIMARY KEY ("ACT_ID") 
);



CREATE TABLE FLIGHT_PACKAGE
(F_PACKAGE_ID NUMBER (4,0),
 FLIGHT_NUM VARCHAR2(8),
 MEAL_TYPE VARCHAR2 (20),
 WIFI NUMBER(1),
 FLIGHT_CLASS VARCHAR2(15),
 CONSTRAINT "F_PKG_ID_PK" PRIMARY KEY ("F_PACKAGE_ID") ,
 CONSTRAINT "FLIGHT_FK" FOREIGN KEY ("FLIGHT_NUM")
 REFERENCES FLIGHTS("FLIGHT_NUM") ON DELETE CASCADE 
);


DROP TABLE BOOKING;
CREATE TABLE BOOKING
(BOOKING_ID NUMBER(5,0),
HOTEL_ID NUMBER(6,0),
ROOM_TYPE VARCHAR(18),
NUM_OF_ROOMS NUMBER(5),
COST_PER_ROOM NUMBER(6,2),
CONSTRAINT "BOOKING_ID_PK" PRIMARY KEY ("BOOKING_ID") 
);

DROP TABLE RESERVATION;
CREATE TABLE RESERVATION
(RESERVATION_ID NUMBER(5,0),
REST_ID NUMBER (5,0),
RES_START TIMESTAMP(0),
RES_END TIMESTAMP(0),
RES_NUM_OF_PEOPLE NUMBER(5),
CONSTRAINT "RESERVATION_ID_PK" PRIMARY KEY("RESERVATION_ID")
);

CREATE TABLE PACKAGE
(PACKAGE_ID NUMBER (5,0),
 F_PACKAGE_ID NUMBER(4,0),
 TRANSPORT_ID NUMBER(5,0),
 BOOKING_ID NUMBER (5,0),
 RESERVATION_ID NUMBER (5,0),
 NUM_OF_PEOPLE NUMBER (3,0),
 P_TOTAL_COST NUMBER (6,2),
 P_CUST_PAYMENTS NUMBER (6,2),

CONSTRAINT "PACKAGE_ID_PK" PRIMARY KEY ("PACKAGE_ID") ,
CONSTRAINT "F_PACKAGE_ID" FOREIGN KEY ("F_PACKAGE_ID")
 REFERENCES FLIGHT_PACKAGE(F_PACKAGE_ID)ON DELETE CASCADE,
 CONSTRAINT "FK_TRANSPORT_ID" FOREIGN KEY  ("TRANSPORT_ID") 
 REFERENCES TRANSPORTATION(TRANSPORT_ID)ON DELETE CASCADE ,
CONSTRAINT "FK_BOOKING_ID" FOREIGN KEY  ("BOOKING_ID") 
 REFERENCES BOOKING(BOOKING_ID)ON DELETE CASCADE ,
CONSTRAINT "FK_RES_ID" FOREIGN KEY  ("RESERVATION_ID") 
 REFERENCES RESERVATION(RESERVATION_ID)ON DELETE CASCADE 
);


CREATE TABLE CUSTOMER_TABLE
(CUSTOMER_ID NUMBER(5,0),
PACKAGE_ID NUMBER (5,0),
C_FIRST_NAME VARCHAR2(18),
C_LAST_NAME VARCHAR2(18),
C_STREET_ADD VARCHAR2(20),
C_STATE VARCHAR2(3),
    C_ZIP_CODE NUMBER(5),
    C_PHONE_NUM NUMBER(10),
    C_EMAIL VARCHAR2(40),
    CONSTRAINT "CUSTOMER_ID_PK" PRIMARY KEY("CUSTOMER_ID"),
    CONSTRAINT "CUST_FK" FOREIGN KEY ("PACKAGE_ID")
 REFERENCES PACKAGE("PACKAGE_ID")ON DELETE CASCADE 
);



CREATE TABLE ACTIVITY_BOOKING
	(PACKAGE_ID NUMBER (5,0),
   ACTIVITY_ID NUMBER (5,0),
  CONSTRAINT "ACT_BOOKING_PK" PRIMARY KEY (PACKAGE_ID,     ACTIVITY_ID),
   CONSTRAINT "ACT_B_PACK_ID_FK" FOREIGN KEY ("PACKAGE_ID")
  REFERENCES PACKAGE(PACKAGE_ID) ON DELETE CASCADE ,
 CONSTRAINT "ACT_B_ACT_ID_FK" FOREIGN KEY ("ACTIVITY_ID") 
  REFERENCES ACTIVITY(ACT_ID) ON DELETE CASCADE 
);

































    


